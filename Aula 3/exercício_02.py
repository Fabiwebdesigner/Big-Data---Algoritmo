# Utilizando a estrutura do programa anterior, some os dois valores se forem diferentes e multiplique-os se forem iguais.
n1=int(input("Informe o primeiro número: "))
n2=int(input("Informe o segundo número: "))
if n1 == n2:
    print (f"Esses números são iguais, então o produto é {n1*n2}")
elif n1!= n2:
    print(f"Esses números são diferentes, então a soma é {n1+n2}")